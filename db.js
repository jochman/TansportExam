// App state
let state = {
    learnedIds: [],
    xp: 0,
    lastQuizScore: null
};

// Local Storage persistence
function saveStateToStorage() {
    localStorage.setItem('tahbura_learned_ids', JSON.stringify(state.learnedIds));
    localStorage.setItem('tahbura_xp', state.xp);
    if (state.lastQuizScore !== null) {
        localStorage.setItem('tahbura_last_quiz_score', state.lastQuizScore);
    }
}

function loadStateFromStorage() {
    const learned = localStorage.getItem('tahbura_learned_ids');
    if (learned) {
        state.learnedIds = JSON.parse(learned);
    }
    const xp = localStorage.getItem('tahbura_xp');
    if (xp) {
        state.xp = parseInt(xp, 10);
    }
    const score = localStorage.getItem('tahbura_last_quiz_score');
    if (score) {
        state.lastQuizScore = parseInt(score, 10);
    }
}

// Update progress bar in sidebar
function updateProgressStats() {
    const fillEl = document.getElementById('sidebar-progress-fill');
    const percentEl = document.getElementById('sidebar-progress-percent');
    const ratioEl = document.getElementById('sidebar-progress-ratio');

    if (!fillEl || !percentEl || !ratioEl) return;

    const ratio = concepts.length > 0 ? state.learnedIds.length / concepts.length : 0;
    const percent = Math.round(ratio * 100);
    
    fillEl.style.width = `${percent}%`;
    percentEl.textContent = `${percent}%`;
    ratioEl.textContent = `${state.learnedIds.length}/${concepts.length}`;
}

// Theme toggler
function toggleTheme() {
    const currentTheme = document.body.getAttribute('data-theme');
    const newTheme = currentTheme === 'dark' ? 'light' : 'dark';
    document.body.setAttribute('data-theme', newTheme);
    localStorage.setItem('theme', newTheme);
    updateThemeIcon(newTheme);
}

function updateThemeIcon(theme) {
    const icon = document.getElementById('theme-icon');
    if (!icon) return;
    if (theme === 'dark') {
        icon.className = 'fa-solid fa-sun';
    } else {
        icon.className = 'fa-solid fa-moon';
    }
}

// Initialize on DOM load
window.addEventListener('DOMContentLoaded', () => {
    loadStateFromStorage();
    updateProgressStats();
    
    // Apply saved theme
    const savedTheme = localStorage.getItem('theme') || 'light';
    document.body.setAttribute('data-theme', savedTheme);
    updateThemeIcon(savedTheme);

    // Call page-specific initializer if present
    if (typeof initPage === 'function') {
        initPage();
    }
});
